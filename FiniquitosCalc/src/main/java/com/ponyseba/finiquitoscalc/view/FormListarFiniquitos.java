    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int idFiniquito;
        String nombreTrabajador;
        String mesesTrabajadosTotal;
        double feriadoLegalHabil;
        int indemnizacionAniosServicio;
        int indemnizacionVacaciones;
        int totalIndemnizacion;
        
        FiniquitoCalcController finiquitoCalcController = new FiniquitoCalcController();
        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_listaFiniquitos.getModel();
        
        modelo.setRowCount(0);
        
        listaFiniquitos = finiquitoCalcController.buscarFiniquitosPorIdUsuario(sesionUsuario);
        
        for(Finiquito finiquito : listaFiniquitos){
            idFiniquito = finiquito.getIdFiniquito();
            nombreTrabajador = finiquito.getNombreTrabajador();
            mesesTrabajadosTotal = finiquito.getMesesTrabajadosTotal();
            feriadoLegalHabil = finiquito.getFeriadoLegalHabil();
            indemnizacionAniosServicio = finiquito.getIndeminizacionAniosServicio();
            indemnizacionVacaciones = finiquito.getIndemnizacionVacaciones();
            totalIndemnizacion = finiquito.getTotalIndemnizacion();
            
            modelo.addRow(new Object[]{idFiniquito, nombreTrabajador, mesesTrabajadosTotal, feriadoLegalHabil, indemnizacionAniosServicio, indemnizacionVacaciones, totalIndemnizacion});
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

